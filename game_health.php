  <h1>Games & health</h1>
  <div class="information">
    <p>Gaming does not pose specific health risks. It is a leisure activity, just more interactive than reading books or watching television.
      As it involves the use of your eyesight we generally advise players to follow these guidelines:
    </br>
    <ul>
      <li>Always play in a well lit room</li>
      <li>Take regular breaks </li>
      <li>Don’t play a game for hours on end, no matter how compelling it may be.</li>
      <li>Keep a minimum distance from the screen.</li>
    </ul>
    <h3>Is it possible for my child to become addicted to games?</h3>
    <p> Playing video games is not a physical addiction. Children play games because they enjoy them; and some enjoy them more than others. 
    While playing a video game they may be blinkered to everything else that goes on around them (the same goes for watching movies or television).
    </p>
    <h3>Can the violence in video games affect my child’s behaviour?</h3>
    <p> There is no evidence to prove that playing violent video games causes any long-term or lasting increase in aggressiveness or violence. The research on whether video games encourage violence focuses primary on short term effects and is inconsistent. 
    However, in order to ensure that your child does not play games deemed to be too violent please follow the age ratings closely. 
    </p>
