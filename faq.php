    <h3><a name="question_0">What is FPB and why was it built?</a></h3>
    <p>The FPB systems provides parents and caregivers with detailed recommendations regarding the age 
      suitability of game content in the form of age labels and content descriptors on game packages. 
      FPB (Film and Publication Board) has been designed to replace existing national age rating systems 
      with a single system that is identical throughout most of South Africa.
    </p>
    <h3><a name="question_1">How are the games rated?</a></h3>
    <p>FPB uses a combination of content declaration and game review to determine the appropriate FPB rating 
    for each game. Initially, the publisher of a game will complete an online declaration form that is sent to 
    the administrator of the system. The completed form is then reviewed and used as a basis for checking the 
    content of the game. The content declaration aspect of FPB is a significant strength as only the 
    developer/publisher of a game has a complete overview of the content in the game. 
    The overview allows the administrator to focus on sections or aspects of the game that will most likely to 
    affect the rating. This is more efficient and reliable than the administrators attempting a full play through
    (assuming it was possible) as the only basis for the rating. 
    </p>
    <h3><a name="question_2">On which criteria are the ratings based?</a></h3>
    <p>The FPB system was developed and based on existing rating systems in Europe and is supported by the majority
    of relevant Member State Government Agencies. A wide range of academics, consumers and other stakeholder groups 
    have been involved in the drafting of the assessment form and the shaping of the organisation in order to meet 
    varying cultural standards and attitudes across the member states.
    </p>
    <h3><a name="question_3">Is the level of difficulty or quality rated as well?</a></h3>
    <p>No, the rating does not take into account the difficulty level or skills required to play a game. 
    FPB only provides information about the suitability of a game in terms of minor protection. 
    A game rated 3+ can be too difficult for a child to play. We therefore always recommend reading the details 
    of the game content on the packaging to ascertain the level of difficulty. 
    </p>
    <h3><a name="question_4">Where do I find information on the content of a game?</a></h3>
    <p>Descriptions of the content of a game can be found on the packaging. There are also specialized game magazines 
    and websites that provide previews, reviews, rankings and news on the latest games. www.gamespot.com is just one 
    of many examples.
    </p>
    <h3><a name="question_5">Who checks the ratings?</a></h3>
    <p>The Netherlands Institute for the Classification of Audio-visual Media (NICAM) and the Video Standards Council
    (VSC) are the administrators of the FPB system. As long established and highly regarded institute, NICAM has 
    many years of experience in rating audio visual material and has provided advice during the development of the 
    FPB scheme.</p>
    <p>NICAM reviews 3 and 7 games whilst the VSC reviews 12, 16 and 18 rated games. Consumers who disagree with a 
    rating can contact the administrator and file a complaint. 
    </p>
    <h3><a name="question_6">Do all games have a FPB rating?</a></h3>
    <p>Although FPB is a self-regulatory system, every game for the Microsoft, Nintendo and Sony games consoles 
    are FPB rated as well as every PC game from the main European and American publishers. Game retailers usually 
    require the products they sell to be labelled by FPB and, consequently, it is very unusual to find a game for 
    sale not rated by FPB.
    </p>
    <h3><a name="question_7">Do retailers need to take the ratings into account?</a></h3>
    <p>The FPB rating system is a voluntary system. It is only backed by legislation in a few countries. 
    In others, however, FPB is usually treated as being virtually mandatory by nearly all of the game retailers 
    in their company policy. We encourage the public to always notify store managers where the age ratings are clearly 
    being ignored by their staff. 
    </p>
    <h3><a name="question_8">What can I do when I don’t agree with a rating?</a></h3>
    <p>As FPB has been designed to constantly evolve, we very much value your comments or complaints on the system, 
    or the ratings that it delivers.</p>
    <p>Complaints about ratings made by publishers or members of the public that cannot be resolved by the 
    Administrator will be heard by an independent Complaints Board. This board includes experts in child protection 
    and psychology, and, more generally, representatives of a wide cross-section of society. The decision of the 
    Complaints Board on an age rating is final and the independent composition of the Board ensures a fair and 
    consistent ruling. Complaints about rating issues can be made through this website. 
    </p>
    <h3><a name="question_9">Do games have an influence on children?</a></h3>
    <p>The research on the impact of videogames has been focused primarily on violence. Numerous studies have been 
    published, but until today there is no evidence that playing violent video games causes any long-term or lasting 
    increase in aggressiveness or violence among players.
    </p>
    
    <h3><a name="question_10">How long may a child play games?</a></h3>
    <p>As every child is different there is no fixed rule. Gaming as such does not entail specific health risks. 
    It is a leisure activity, just like reading books or watching television. As it involves the use of your eyesight
    we generally advise these guidelines: 
    </p>
    <ul>
      <li>Always play in a well lit room</li>
      <li>Take regular breaks</li>
      <li>Don’t play a game for hours on end, no matter how compelling it may be.</li>
      <li>Keep a minimum distance from the screen.</li>
    </ul>
    <h3><a name="question_11">What is FPB Online?</a></h3>
    <p>FPB Online is an addition to the FPB system. Upon joining FPB Online companies must sign up to a code of 
    conduct, thus indicating that they manage the online gaming features of their products in a responsible manner. 
    In return they receive a license to use a registered FPB Online label as a “seal of quality” 
    </p>
    <h3><a name="question_12">What is the difference between the FPB rating system and FPB Online?</a></h3>
    <p>The FPB age rating system issues licenses to use a specific age class label and descriptor for a specific 
    product on a specific platform. The FPB Online label, however, underlines the dedication to the protection of 
    minors in a company’s policy regarding online gaming. It indicates membership of a network of companies that 
    comply with the requirements of the FPB Online Safety Code.
    </p>
    <h3><a name="question_13">Why is the FPB Online label also displayed on the packages of games?</a></h3>
    <p>The use of the FPB Online label as an additional descriptor on the back of the package is compulsory when 
    details of online gameplay feature in the FPB rating questionnaire. 
    </p>
