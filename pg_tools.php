
<h1>Parental Control Tools</h1>
  <div class="information">
    <p>Parental control locks allow parents and caregivers to control or limit access to games and other forms of entertainment. 
       Console and games system manufacturers offer parents varying methods of controlling access. 
       Typically including controls based directly on a game’s age rating
    </p>
    <ul>
      <li>Controls that change and set restriction to decide, case by case, which games your children can play.</li>
      <li>controlling access to the internet from consoles and handheld devices</li>
      <li>controlling the amount of time that children can spend on the console</li>      
    </ul>
     <p>You will find all the information you need on parental locks in the literature that comes with the system you have purchased.  
    </p>
    
  </div>
